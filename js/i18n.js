let currentLang = localStorage.getItem('lang') || 'es';

function setLang(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    translatePage();
    updateLangSwitcher();
}

function translatePage() {
    const d = translations[currentLang];

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (d[key]) {
            el.innerHTML = d[key];
        }
    });

    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
        const key = el.getAttribute('data-i18n-ph');
        if (d[key]) {
            el.placeholder = d[key];
        }
    });
}

function updateLangSwitcher() {
    const esBtn = document.getElementById('lang-es');
    const enBtn = document.getElementById('lang-en');
    if (esBtn && enBtn) {
        if (currentLang === 'es') {
            esBtn.style.fontWeight = '600';
            esBtn.style.color = 'var(--black)';
            enBtn.style.fontWeight = '400';
            enBtn.style.color = 'var(--grey3)';
        } else {
            enBtn.style.fontWeight = '600';
            enBtn.style.color = 'var(--black)';
            esBtn.style.fontWeight = '400';
            esBtn.style.color = 'var(--grey3)';
        }
    }
}

document.addEventListener('DOMContentLoaded', () => {
    translatePage();
    updateLangSwitcher();
});
